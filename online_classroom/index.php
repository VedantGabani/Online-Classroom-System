                    <h4 id="about-the-department-heading">About the department</h4>
                    <p class="text-justify" id="about-the-department-content">The Department of Computer Engineering offers programs in B.E.(Computer) since 1995.Initially the intake was 60. With the inception of the same course in the second shift in 2013, the total intake is now 120. The Department of Computer
                        Engineering also offers PG course with the specialization in 'Computer Engineering'. The course commenced in the year 2012 with intake of 18. Both these programs are approved by Gujarat Technological University, Chandkheda, Gandhinagar
                        and AICTE.<br><br>The department imparts high quality technical education to its students with the help of its state-of-the-art computing facilities and highly qualified teaching staff as well as supporting staff. The department
                        offers bachelor's degree in computer engineering that covers the latest subjects in the variety of areas like computer hardware, software, networking, microprocessors, operating systems, database management, artificial intelligence,
                        information security, web technologies etc. The department continues the journey towards "The goal of creating technical excellence which imparts the best knowledge and skills to students with the objective of producing high calibre
                        engineering graduates". A sound theoretical and practical work prepares the students in wider area of computer engineering.<br><br>There are six well-equipped laboratories in the department. The ultimate laboratory facility along
                        with unlimited internet access helps the upcoming computer engineer to come out with bright future. The department consists of highly capable teaching and non-teaching staff who are always ready to inculcate the best knowledge
                        to the students. The department always strives for providing excellent and modernizes laboratory facilities to the students.</p>
